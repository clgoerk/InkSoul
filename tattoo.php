        <h3>Tattoo Aftercare</h3>

        <p><strong>Leave the Bandage On:</strong> Keep the bandage or wrap on for 2–6 hours after your session to protect it from bacteria.</p>
        <p><strong>Clean Gently:</strong> Wash your hands, then clean the tattoo with lukewarm water and mild, fragrance-free soap. Pat dry with a clean towel.</p>
        <p><strong>Apply Moisturizer:</strong> Use a thin layer of fragrance-free ointment like Aquaphor or a tattoo-specific aftercare cream 2–3 times a day.</p>
        <p><strong>Avoid Soaking:</strong> No swimming, baths, or hot tubs for at least 2 weeks. Showers are fine, but avoid direct water pressure on the tattoo.</p>
        <p><strong>Don’t Scratch or Pick:</strong> Peeling and itching are normal. Do not scratch or pick scabs as this can affect the healing process and ink retention.</p>
        <p><strong>Wear Loose Clothing:</strong> Tight clothes can irritate the tattoo and slow healing. Choose breathable, clean fabrics.</p>
        <p><strong>Avoid Sun Exposure:</strong> Keep the tattoo out of direct sunlight. After it’s healed, always use high-SPF sunscreen to prevent fading.</p>
        <p><strong>Watch for Infection:</strong> Redness, swelling, or pus could indicate infection. If symptoms persist or worsen, contact your tattoo studio or doctor.</p>
